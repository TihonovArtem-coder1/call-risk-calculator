# Список вопросов (6 вопросов)
questions = [
    "Звонящий просит код из СМС, пароль или CVV-код карты?",
    "Звонящий торопит, говорит 'решать нужно прямо сейчас'?",
    "Звонящий представляется сотрудником банка, МВД, ФСБ?",
    "Звонящий просит не вешать трубку и не звонить родным?",
    "Звонящий просит перевести деньги на 'безопасный счёт'?",
    "Номер неизвестный, с кодом другой страны или скрыт?",
]

# Список баллов риска (6 чисел, соответствуют вопросам выше)
risk_points = [3, 2, 1, 2, 3, 1]

print("=" * 60)
print("КАЛЬКУЛЯТОР РИСКА ТЕЛЕФОННОГО ЗВОНКА")
print("=" * 60)
print()
print("Ответьте на вопросы о звонке, который вам поступил.")
print("Вводите: 1 = Да, 0 = Нет")
print()

# Переменная для подсчёта баллов риска
risk_score = 0

# ============================================================================
# ЦИКЛ ПО ВОПРОСАМ
# ============================================================================
for i in range(6):
    # Выводим вопрос номер i+1 (чтобы было с 1, а не с 0)
    print(f"ВОПРОС {i + 1} из 6: {questions[i]}")

    # ========================================================================
    # ПРОВЕРКА ВВОДА (цикл while)
    # ========================================================================
    while True:
        answer = input("Ваш ответ (1 = Да, 0 = Нет): ")

        # Проверяем, ввёл ли пользователь 1 или 0
        if answer == "1" or answer == "0":
            break  # Ввод правильный — выходим из цикла while
        else:
            print("⚠️ Ошибка! Введите только 1 или 0!")
            print("   Попробуйте ещё раз.")

    # ========================================================================
    # ПОДСЧЁТ БАЛЛОВ
    # ========================================================================
    if answer == "1":
        risk_score = risk_score + risk_points[i]
        print(f"⚠️ +{risk_points[i]} балла риска")
    else:
        print("✓ 0 баллов. Хорошо.")

    print()
    print("-" * 60)
    print()

# ============================================================================
# ВЫВОД РЕЗУЛЬТАТОВ
# ============================================================================
print("=" * 60)
print("РЕЗУЛЬТАТЫ АНАЛИЗА")
print("=" * 60)
print(f"Общий балл риска: {risk_score} из 12")
print()

print("ВЕРДИКТ:")
print("-" * 60)

if risk_score >= 8:
    print("🔴 ВЫСОКИЙ РИСК!")
    print("   Скорее всего, это мошенники.")
    print("   РЕКОМЕНДАЦИИ:")
    print("   • Немедленно прекратите разговор")
    print("   • Не сообщайте никакие данные")
    print("   • Перезвоните в организацию по официальному номеру")
    print("   • Заблокируйте номер")
elif risk_score >= 4:
    print("🟡 СРЕДНИЙ РИСК!")
    print("   Звонок подозрительный, нужна проверка.")
    print("   РЕКОМЕНДАЦИИ:")
    print("   • Не принимайте решений по телефону")
    print("   • Проверьте информацию через официальные каналы")
    print("   • Посоветуйтесь с родными")
else:
    print("🟢 НИЗКИЙ РИСК!")
    print("   Скорее всего, звонок безопасен.")
    print("   РЕКОМЕНДАЦИИ:")
    print("   • Всё равно не сообщайте пароли и коды")
    print("   • Будьте внимательны к деталям разговора")

print()
print("=" * 60)
